    //retorna o registro a partir de um indice da arraylist
    public void posv(int pos) {
        nome.setText(((Contatos) contatos.get(pos)).getNome());
        endereco.setText(((Contatos) contatos.get(pos)).getEndereco());
        email.setText(((Contatos) contatos.get(pos)).getemail());
        id.setText("" + ((Contatos) contatos.get(pos)).getId());
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        String date = sdf.format((((Contatos) contatos.get(pos)).getDataNascimento()).getTime());
        data.setText(date);
    }
    
