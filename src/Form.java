    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        int d,m,a;
        
        ContatoDao banco = new ContatoDao();
        (contatos.get(pos)).setNome(nome.getText());
        (contatos.get(pos)).setEndereco(endereco.getText());
        (contatos.get(pos)).setemail(email.getText());
        Calendar nasc = Calendar.getInstance();
        d=Integer.parseInt(data.getText().split("/")[0]);
        a=Integer.parseInt(data.getText().split("/")[2]);
        m=Integer.parseInt(data.getText().split("/")[1]);
        nasc.set(a, m, d);
        (contatos.get(pos)).setDataNascimento(nasc);
        banco.altera(contatos.get(pos));
        
        JOptionPane.showMessageDialog(null,"Registro alterado","", JOptionPane.INFORMATION_MESSAGE);
        habl();

        alterar.setEnabled(true);
        salvar.setEnabled(true);
    }//GEN-LAST:event_salvarActionPerformed

